const INNATE_RELIC_ID_SET = new Set(INNATE_RELICS.map((relic) => relic.id));

export function isInnateRelicId(relicId: string | null | undefined): boolean {
  return typeof relicId === "string" && INNATE_RELIC_ID_SET.has(relicId);
}

export function mergeWithInnateRelics(userInventory: Relic[]): Relic[] {
  const filteredUserInventory = userInventory.filter((relic) => !INNATE_RELIC_ID_SET.has(relic.id));
  return [...INNATE_RELICS, ...filteredUserInventory];
}
